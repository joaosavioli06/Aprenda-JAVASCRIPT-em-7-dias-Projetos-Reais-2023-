//Operadores de comparação básicos
// > Maior
// < Menor
// >= Maior ou igual
// <= Menor ou igual
// === Valor idêntico
// !== Valor diferente

let num1 = 10

console.log(num1 > 5) // true
console.log(num1 < 5) // false
console.log(num1 >= 5) // true
console.log(num1 <= 5) // false
console.log(num1 === 10) // true
console.log(num1 !== 10) // false