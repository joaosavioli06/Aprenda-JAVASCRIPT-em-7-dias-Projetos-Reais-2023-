// Aqui temos um do while loop
// Ele não é muito utilizado

i = 1

do {
    console.log('Número', i)
    i++
} while(i <= 10)

// Ele funciona assim que nem os outros tipos de loop
// Primeiro você define a variável fora da operação
// Depois dentro do "do", você coloca entre {} o que você quer que ele faça
// No final da {}, você coloca o "while"