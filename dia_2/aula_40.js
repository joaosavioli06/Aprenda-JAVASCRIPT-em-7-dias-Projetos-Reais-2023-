// Aqui utilizaremos um break em um loop

let i = 1

while (i <= 10){
    if (i === 8) break
    console.log('Número', i)
    i++
}

// Podemos ver que a única linha adicionda é essa aqui:
// if (i === 8) break = determinamos um número para que o loop pare neste número

// Quando detectar o 8, o loop irá parar. Por isso o loop vai até o 7


