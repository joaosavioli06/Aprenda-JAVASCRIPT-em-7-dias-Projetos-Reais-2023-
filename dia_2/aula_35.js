// Em um for loop, é necessário fornecer três informações:
// 1ª - Variável
// 2ª - Condição
// 3ª - Incremento

for (i = 1; i <= 10; i++){
    console.log('Número ' + i)
}

// Aqui eu defini a variável, que é a letra i (chamamos de index)
// Depois eu coloquei a condicação, enquanto a variável i for menor ou igual a 10
// Depois o incremento, ou também ação, '++', que vai adicionando mais um 