// Como fazer um while loop?
// Ele faz a mesma coisa que o for loop, só que com algumas coisas diferentes

let i = 1

// Você define a variável antes de começar a operação

while (i <= 10) {
    console.log('Número', i)
    i++
}

// (i >= 10) = define que o index tem que ir até o número 10
// console.log('Número', i) = define o que deve retornar no terminal
// i++ = Ir contando de um mais um 