// O que são Objetos Built In?
// São objetos que possuem prédefinições de códigos já criados no passado, para agilizar e facilitar a execução de algumas tarefas

// Exemplos aqui:

console.log(Math.PI)
// Exibe o valor de PI
console.log(Math.max(1,4,5,8,123,221,98,192,670))
// Mostra o valor maior dentre os números escolhidos
console.log(Math.min(9,4,6,8,10,2))
// Mostra o menor valor dentre os números escolhidos
console.log(Math.round(9.5))
// Arredonda números