// Como localizar um item em uma array primitiva?
// Você pode utilizar o 'indexOf' e o 'includes' para isso

const num = [5, 6, 7, 8]

// o 'indexOf' te mostra o número index onde o item está

console.log(num.indexOf(8)) 

// o 'includes' só retorna em boolean se o items está ou não na array

console.log(num.includes(8))