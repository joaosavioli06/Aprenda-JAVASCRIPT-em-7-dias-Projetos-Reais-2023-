// O que é o Join Array?

// O Join Array é um recurso para adicionar alguma coisa dentro de uma array
// Exemplo abaixo:

let lista = ['Endrick', 'Gerson', 'Rubens', 'Kelly']

// Aqui eu criei uma variável que recebe a array 'lista' junto com uma função 'join'
// Essa função serve para adicionar alguma coisa na array, transformando ela em string

let listaJoin = lista.join(', ')

// Eu adicionei uma vírgula do lado de cada item da array

