// Como reverter, colocar em ordem alfabética e numérica a sua array?

// Reverter = reverse()
// Ordem alfabética e numérica = sort()

let abc = ['João', 'Caio', 'André', 'Beatriz', 'Dante']

// Função para colocar tudo em ordem alfabética
abc.sort()

console.log(abc)

let cba = ['Ana', 'Beatriz', 'Cauan', 'Danilo', 'Eduardo']

// Função para colocar tudo em ordem reversa
cba.reverse()

console.log(cba)

let num = [1, 4 , 5, 3 , 2]

// Função serve para deixar tudo em ordem numérica
num.sort()

console.log(num)